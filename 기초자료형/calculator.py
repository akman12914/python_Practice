firstNumber = int(input("첫 번째 숫자를 입력하세요:"));
secondNumber = int(input("두 번째 숫자를 입력하세요:"));

print(firstNumber, "+" , secondNumber, "=", (firstNumber + secondNumber));
print(firstNumber, "-" , secondNumber, "=", (firstNumber - secondNumber));
print(firstNumber, "*" , secondNumber, "=", (firstNumber * secondNumber));
print(firstNumber, "/" , secondNumber, "=", (firstNumber / secondNumber));